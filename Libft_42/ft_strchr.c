#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	b;
	int		i;

	b = c;
	i = 0;
	while (s[i] != b && s[i] != '\0')
		i++;
	if (s[i] == '\0' && b != '\0')
		return (NULL);
	return ((char *) s + i);
}
